function scorePathForQuery(path, query) {
  var q = query.toLowerCase();
  var score = 0;
  var words = q.split(/[\s,|/]+/).filter(function (w) { return w.length > 1; });
  (AI_PATH_KEYWORDS[path.id] || []).forEach(function (kw) {
    if (q.indexOf(kw) >= 0) score += 4;
    words.forEach(function (w) { if (kw.indexOf(w) >= 0 || w.indexOf(kw) >= 0) score += 2; });
  });
  path.skills.forEach(function (s) {
    if (q.indexOf(s.toLowerCase()) >= 0) score += 5;
    words.forEach(function (w) { if (s.toLowerCase().indexOf(w) >= 0) score += 3; });
  });
  if (path.title.toLowerCase().indexOf(q) >= 0) score += 6;
  if (path.tag.toLowerCase().indexOf(q) >= 0) score += 4;
  words.forEach(function (w) {
    if (path.description.toLowerCase().indexOf(w) >= 0) score += 1;
  });
  return score;
}

window.runAIMatch = function () {
  var input = document.getElementById('ai-match-input');
  var out = document.getElementById('ai-match-result');
  var term = document.getElementById('ai-match-terminal');
  if (!input || !out || typeof PATHS === 'undefined') return;
  var q = input.value.trim();
  if (!q) { out.innerHTML = '<p class="ai-match-hint">Type skills like Python, React, SQL, or Cybersecurity…</p>'; return; }

  var ranked = PATHS.map(function (p) { return { path: p, score: scorePathForQuery(p, q) }; })
    .sort(function (a, b) { return b.score - a.score; });
  var top = ranked.filter(function (r) { return r.score > 0; }).slice(0, 3);
  if (!top.length) top = ranked.slice(0, 2);

  if (term) {
    term.innerHTML = '';
    addTermLine('Analyzing skills: ' + q);
    setTimeout(function () { addTermLine('Matching against ' + PATHS.length + ' career paths…'); }, 400);
    setTimeout(function () { addTermLine('Top match: ' + top[0].path.title + ' (' + top[0].score + '% fit)'); }, 900);
  }

  out.innerHTML = top.map(function (r, i) {
    var p = r.path;
    var pct = Math.min(98, Math.max(35, r.score * 8 + 40));
    return '<div class="ai-match-card" style="--accent:' + p.color + '">' +
      '<div class="ai-match-rank">#' + (i + 1) + ' match · ' + pct + '% fit</div>' +
      '<div class="ai-match-title">' + p.icon + ' ' + p.title + '</div>' +
      '<p class="ai-match-desc">' + p.description + '</p>' +
      '<div class="ai-match-actions">' +
        '<button type="button" class="yc-pill yc-pill-filled" onclick="showDetail(\'' + p.id + '\')">View roadmap</button>' +
        '<a href="demo.html?path=' + encodeURIComponent(p.id) + '" class="yc-pill">Sync to ResumeAI</a>' +
      '</div></div>';
  }).join('');
};

function addTermLine(text) {
  var term = document.getElementById('ai-match-terminal');
  if (!term) return;
  var line = document.createElement('div');
  line.className = 'ai-t-line';
  line.innerHTML = '<span class="ai-t-prompt">&gt;</span> ' + text;
  term.appendChild(line);
  term.scrollTop = term.scrollHeight;
}

